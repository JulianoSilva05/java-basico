public class OperadorQuatro {
    public static void main(String[] args) {
        int numero = 5;
        //x repetições
        System.out.println(numero++);
        numero = numero + 1;
        System.out.println(numero);

        //Boleano
        boolean variavel = true;
        variavel = !variavel;
        System.out.println(variavel);
    }
}
